namespace VEMS.AMathCore
{
    /// <summary>
    /// complex number representation format
    /// </summary>
    public enum ComplexFormat
    {
        /// <summary>
        /// represent complex number in its
        /// real- and imag-parts
        /// </summary>
        RealAndImaginary = 0,

        /// <summary>
        /// represent complex number in its
        /// magnitude and argument
        /// </summary>
        MagnitudeAndPhase = 1
    }
}
