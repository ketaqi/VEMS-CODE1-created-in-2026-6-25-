namespace VEMS.MathCore
{

    /// <summary>
    /// type of reference point for grid
    /// </summary>
    public enum GridRefType
    {
        /// <summary>
        /// reference is set as the start point
        /// </summary>
        Start,

        /// <summary>
        /// reference is set as the center point
        /// </summary>
        Center,

        /// <summary>
        /// reference is set as the lower bound of the range
        /// </summary>
        LowerBound,

        /// <summary>
        /// reference is set as the upper bound of the range
        /// </summary>
        UpperBound,
    }

}
